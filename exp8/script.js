function changeBgOnHover() {
    document.getElementById('hoverBox').style.backgroundColor = '#ff6347';
}

function resetBg() {
    document.getElementById('hoverBox').style.backgroundColor = '#007bff';
}

function changeBgOnFocus() {
    document.body.style.backgroundColor = '#87ceeb';
}

function resetBgOnBlur() {
    document.body.style.backgroundColor = '#f5f5f5';
}